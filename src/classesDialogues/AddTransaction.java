    public AddTransaction(java.awt.Frame parent, boolean modal,Transaction trans) {
        super(parent, modal);
        initComponents();
        jTransAdd.setText("Modifica");
        Calendar cal = Calendar.getInstance();
        cal.setTime(trans.getData());
        jTransDate.setSelectedDate(cal);
        jTransDisc.setText(String.valueOf(trans.getDiscount()));
        jTransValue.setText(String.valueOf(trans.getValue()));
        jTransDescr.setText(trans.getDescription());
        getClientsNames();
        int i=0,found=0;
        try {
            while(rezultat.next())
            {
                    jTransClient.addItem(rezultat.getString(1));
                    if(rezultat.getString(1).equals(trans.getClient())) 
                    {
                        jTransClient.setSelectedIndex(i);
                        found=1;
                    }
                    i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddTransaction.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(found ==0)
        {
            jTransClient.addItem(trans.getClient());
            jTransClient.setSelectedIndex(i);
        }
        for(i=0;i<jTransType.getItemCount();i++)
        {
            if(jTransType.getItemAt(i).equals(Constants.getType(trans.getType()))) {
                jTransType.setSelectedIndex(i);
                break;
            }
        }
    }
