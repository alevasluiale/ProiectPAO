    private void jTransAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTransAddActionPerformed
        // TODO add your handling code here:
            Transaction trans = new Transaction();
            trans.setData(jTransDate.getSelectedDate().getTime());
            trans.setType(Constants.getType(jTransType.getSelectedItem().toString()));
            trans.setClient(jTransClient.getSelectedItem().toString());
            trans.setDescription(jTransDescr.getText());
            trans.setValue(Double.parseDouble(jTransValue.getText().replaceAll(" lei", "")));
            trans.setDiscount(Double.parseDouble(jTransDisc.getText().replaceAll(" %", "")));
        
        if(jTransAdd.getText().equals("Adauga") ) DataManager.addTransaction(trans);
        else DataManager.modifyTransaction(trans);
        this.dispose();
    }//GEN-LAST:event_jTransAddActionPerformed

